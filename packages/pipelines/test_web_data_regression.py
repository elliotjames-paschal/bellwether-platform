# ============================================================
# SNAPSHOT
# ============================================================

def snapshot(overwrite: bool = False):
    """Copy current JSON outputs to snapshot directory."""
    if SNAPSHOT_DIR.exists() and not overwrite:
        print(f"Snapshot already exists at {SNAPSHOT_DIR}")
        print("Use --snapshot --overwrite to replace it.")
        return

    SNAPSHOT_DIR.mkdir(parents=True, exist_ok=True)

    copied = 0
    for fname in RESEARCH_TAB_FILES:
        src = WEB_DATA_DIR / fname
        if src.exists():
            shutil.copy2(src, SNAPSHOT_DIR / fname)
            copied += 1
        else:
            print(f"  SKIP {fname} (not found)")

    # Save metadata
    meta = {
        "timestamp": datetime.now().isoformat(),
        "files_copied": copied,
        "source_dir": str(WEB_DATA_DIR),
    }
    with open(SNAPSHOT_DIR / "_metadata.json", "w") as f:
        json.dump(meta, f, indent=2)

    print(f"Snapshot saved: {copied} files -> {SNAPSHOT_DIR}")


