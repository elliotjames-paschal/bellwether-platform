# SCHEMA FINGERPRINTING
# ============================================================

def fingerprint(obj, path=""):
    """Recursively fingerprint a JSON object's structure."""
    info = {"type": type(obj).__name__}

    if isinstance(obj, dict):
        info["keys"] = sorted(obj.keys())
        info["children"] = {k: fingerprint(v, f"{path}.{k}") for k, v in obj.items()}
    elif isinstance(obj, list):
        info["length"] = len(obj)
        if obj:
            # Fingerprint first element as representative
            info["element_sample"] = fingerprint(obj[0], f"{path}[0]")
    elif isinstance(obj, (int, float)):
        if math.isnan(obj) or math.isinf(obj):
            info["anomaly"] = "NaN" if math.isnan(obj) else "Inf"
    elif obj is None:
        info["anomaly"] = "null"

    return info


def count_anomalies(obj, path=""):
    """Count NaN, Infinity, and null values in a JSON structure."""
    anomalies = []

    if isinstance(obj, dict):
        for k, v in obj.items():
            anomalies.extend(count_anomalies(v, f"{path}.{k}"))
    elif isinstance(obj, list):
        for i, v in enumerate(obj):
            anomalies.extend(count_anomalies(v, f"{path}[{i}]"))
    elif isinstance(obj, float):
        if math.isnan(obj):
            anomalies.append(f"{path}: NaN")
        elif math.isinf(obj):
            anomalies.append(f"{path}: Infinity")
    elif obj is None:
        anomalies.append(f"{path}: null")

    return anomalies


